class Faculty:
	id = -1
	salary = -1.0	
	firstName = ""
	lastName = ""
	emailAddress = ""
	phoneNumber = ""
	address = ""
	
	
	def __init__(self, id, salary, firstName, lastName, emailAddress, phoneNumber, address):
		self.id = id
		self.salary = salary	
		self.firstName = firstName
		self.lastName = lastName
		self.emailAddress = emailAddress
		self.phoneNumber = phoneNumber
		self.address = address
		
