class Student:
	id = -1	
	gpa = -1.0
	firstName = ""
	lastName = ""
	emailAddress = ""
	phoneNumber = ""
	address = ""
	
	
	
	def __init__(self, id, gpa, firstName, lastName, emailAddress, phoneNumber, address):
		self.id = id	
		self.gpa = gpa
		self.firstName = firstName
		self.lastName = lastName
		self.emailAddress = emailAddress
		self.phoneNumber = phoneNumber
		self.address = address

		
